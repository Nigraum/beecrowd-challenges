a = float(input())
b = float(input())
c = float(input())
triangleArea = (a * c) / 2
circleArea = 3.14159 * c ** 2
trapezeArea = ((a + b) * c) / 2
squareArea = b ** 2
rectangleArea = a * b
print('TRIANGULO: %.3f' %triangleArea)
print('CIRCULO: %.3f' %circleArea)
print('TRAPEZIO: %.3f' %trapezeArea)
print('QUADRADO: %.3f' %squareArea)
print('RETANGULO: %.3f' %rectangleArea)
